			Bij de invoering van de eenheidsstructuur werd er geopteerd om alle leerlingen op een locatie samen te brengen op de Markt, waardoor de locatie van het vroegere college werd verlaten en er nog een middenschool overbleef: het Instituut Zusters Maricolen.
			Op dit ogenblik telt de school meer dan 750 leerlingen en biedt ze werkgelegenheid aan meer dan 90 personeelsleden. De leerlingen zijn niet alleen meer afkomstig uit Maldegem, maar vanuit alle randgemeenten en de wijde omgeving komen wagens en autobussen elke ochtend jongeren aanvoeren om onderwijs te volgen in Maldegem.
